nome = "Adson Mozart Santos Paixao"

#Esboço [start:stop:step]

print(nome[0]) #Pega o caracter do indice informado

print(nome[:4]) #Pega do início até o indice informado

print(nome[2:]) #Pega do final até o indice informado

print(nome[9:25]) #Pega um trecho de um índice a outro

print(nome[9:25:2]) #Pega um trecho de um índice a outro com o intervalo informado

print(nome[:]) #Retorna a string inteira

print(nome[::-1]) #Ele não vai pegar o start, nem o stop, só vai pegar a string e reproduzir ao contrário

